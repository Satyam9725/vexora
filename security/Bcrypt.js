class BcryptEngine {
  /**
   * Helper function: Blowfish Encipher
   */
  static _encipher(lr, P, S) {
    let l = lr[0];
    let r = lr[1];

    for (let i = 0; i < 16; i += 2) {
      l ^= P[i];
      r ^= S[0][l >>> 24] + S[1][(l >>> 16) & 0xff] ^ S[2][(l >>> 8) & 0xff] + S[3][l & 0xff];
      r = r >>> 0;
      r ^= P[i + 1];
      l ^= S[0][r >>> 24] + S[1][(r >>> 16) & 0xff] ^ S[2][(r >>> 8) & 0xff] + S[3][r & 0xff];
      l = l >>> 0;
    }

    l ^= P[16];
    r ^= P[17];

    lr[0] = r >>> 0;
    lr[1] = l >>> 0;
  }

  static _expandKey(P, S, salt, key) {
    const keyLen = key.length;
    const saltLen = salt.length;

    let keyIdx = 0;
    for (let i = 0; i < 18; i++) {
      let data = 0;
      for (let j = 0; j < 4; j++) {
        data = (data << 8) | (keyLen > 0 ? key[keyIdx % keyLen] : 0);
        keyIdx++;
      }
      P[i] ^= data >>> 0;
    }

    const lr = new Uint32Array(2);
    let saltIdx = 0;

    for (let i = 0; i < 18; i += 2) {
      if (saltLen > 0) {
        let d0 = 0, d1 = 0;
        for (let j = 0; j < 4; j++) {
          d0 = (d0 << 8) | salt[saltIdx % saltLen];
          saltIdx++;
          d1 = (d1 << 8) | salt[saltIdx % saltLen];
          saltIdx++;
        }
        lr[0] ^= d0 >>> 0;
        lr[1] ^= d1 >>> 0;
      }
      this._encipher(lr, P, S);
      P[i] = lr[0];
      P[i + 1] = lr[1];
    }

    for (let i = 0; i < 4; i++) {
      for (let j = 0; j < 256; j += 2) {
        if (saltLen > 0) {
          let d0 = 0, d1 = 0;
          for (let k = 0; k < 4; k++) {
            d0 = (d0 << 8) | salt[saltIdx % saltLen];
            saltIdx++;
            d1 = (d1 << 8) | salt[saltIdx % saltLen];
            saltIdx++;
          }
          lr[0] ^= d0 >>> 0;
          lr[1] ^= d1 >>> 0;
        }
        this._encipher(lr, P, S);
        S[i][j] = lr[0];
        S[i][j + 1] = lr[1];
      }
    }
  }

  /**
   * EksBlowfish Setup (Cost, Salt, Key)
   */
  static _initKey(cost, salt, key) {
    const P = new Uint32Array(P_ORIG);
    const S = S_ORIG.map(s => new Uint32Array(s));

    this._expandKey(P, S, salt, key);

    const zeroBytes = new Uint8Array(0);
    const rounds = 1 << cost;
    for (let r = 0; r < rounds; r++) {
      this._expandKey(P, S, zeroBytes, key);
      this._expandKey(P, S, zeroBytes, salt);
    }

    return { P, S };
  }

  /**
   * Encode 16 bytes salt/hash to Bcrypt Base64
   */
  static encodeBase64(b, len) {
    let off = 0;
    let rs = "";
    while (off < len) {
      let c1 = b[off++] & 0xff;
      rs += BCRYPT_CHARS[(c1 >> 2) & 0x3f];
      c1 = (c1 & 0x03) << 4;
      if (off >= len) {
        rs += BCRYPT_CHARS[c1 & 0x3f];
        break;
      }
      let c2 = b[off++] & 0xff;
      c1 |= (c2 >> 4) & 0x0f;
      rs += BCRYPT_CHARS[c1 & 0x3f];
      c1 = (c2 & 0x0f) << 2;
      if (off >= len) {
        rs += BCRYPT_CHARS[c1 & 0x3f];
        break;
      }
      c2 = b[off++] & 0xff;
      c1 |= (c2 >> 6) & 0x03;
      rs += BCRYPT_CHARS[c1 & 0x3f];
      rs += BCRYPT_CHARS[c2 & 0x3f];
    }
    return rs;
  }

  /**
   * Decode Bcrypt Base64 back to Uint8Array
   */
  static decodeBase64(s, len) {
    let off = 0;
    let slen = s.length;
    let b = new Uint8Array(len);
    let bOff = 0;

    while (off < slen && bOff < len) {
      let c1 = BCRYPT_CHARS.indexOf(s[off++]);
      let c2 = BCRYPT_CHARS.indexOf(s[off++]);
      if (c1 === -1 || c2 === -1) break;

      b[bOff++] = (c1 << 2) | ((c2 & 0x30) >> 4);
      if (bOff >= len || off >= slen) break;

      let c3 = BCRYPT_CHARS.indexOf(s[off++]);
      if (c3 === -1) break;

      b[bOff++] = ((c2 & 0x0f) << 4) | ((c3 & 0x3c) >> 2);
      if (bOff >= len || off >= slen) break;

      let c4 = BCRYPT_CHARS.indexOf(s[off++]);
      if (c4 === -1) break;

      b[bOff++] = ((c3 & 0x03) << 6) | c4;
    }

    return b;
  }

  /**
   * Generate $2y$ / $2b$ Bcrypt Hash (PHP-compatible)
   * Example: $2y$10$e8w.x.s62Gj77p0P0...
   */
  static hash(password, cost = 10, prefix = "$2y$") {
    if (!password) throw new Error("Password cannot be empty");
    if (cost < 4 || cost > 31) throw new Error("Bcrypt cost must be between 4 and 31");

    const saltBytes = crypto.randomBytes(16);
    const passBytes = Buffer.from(password, "utf8").subarray(0, 72);

    const { P, S } = this._initKey(cost, saltBytes, passBytes);

    // Cipher text "OrpheanBeholderScryDoubt" (24 bytes)
    const ciphertext = new Uint32Array([
      0x4f727068, 0x65616e42, 0x65686f6c, 0x64657253, 0x63727944, 0x6f756274
    ]);

    const lr = new Uint32Array(2);
    for (let i = 0; i < 64; i++) {
      for (let j = 0; j < 6; j += 2) {
        lr[0] = ciphertext[j];
        lr[1] = ciphertext[j + 1];
        this._encipher(lr, P, S);
        ciphertext[j] = lr[0];
        ciphertext[j + 1] = lr[1];
      }
    }

    // Convert ciphertext uint32s back to bytes
    const hashBytes = new Uint8Array(24);
    for (let i = 0; i < 6; i++) {
      hashBytes[i * 4] = (ciphertext[i] >> 24) & 0xff;
      hashBytes[i * 4 + 1] = (ciphertext[i] >> 16) & 0xff;
      hashBytes[i * 4 + 2] = (ciphertext[i] >> 8) & 0xff;
      hashBytes[i * 4 + 3] = ciphertext[i] & 0xff;
    }

    const costStr = cost < 10 ? "0" + cost : String(cost);
    const saltEncoded = this.encodeBase64(saltBytes, 16);
    const hashEncoded = this.encodeBase64(hashBytes, 23);

    return `${prefix}${costStr}$${saltEncoded}${hashEncoded}`;
  }

  /**
   * Verify password against a $2y$ / $2b$ / $2a$ Bcrypt Hash (PHP-compatible)
   */
  static verify(password, hashedPassword) {
    if (!password || !hashedPassword) return false;

    const match = hashedPassword.match(/^\$(2[ayb])\$(\d\d)\$([./A-Za-z0-9]{22})([./A-Za-z0-9]{31})$/);
    if (!match) return false;

    const [, prefix, costStr, saltEncoded] = match;
    const cost = parseInt(costStr, 10);
    const saltBytes = this.decodeBase64(saltEncoded, 16);
    const passBytes = Buffer.from(password, "utf8").subarray(0, 72);

    const { P, S } = this._initKey(cost, saltBytes, passBytes);

    const ciphertext = new Uint32Array([
      0x4f727068, 0x65616e42, 0x65686f6c, 0x64657253, 0x63727944, 0x6f756274
    ]);

    const lr = new Uint32Array(2);
    for (let i = 0; i < 64; i++) {
      for (let j = 0; j < 6; j += 2) {
        lr[0] = ciphertext[j];
        lr[1] = ciphertext[j + 1];
        this._encipher(lr, P, S);
        ciphertext[j] = lr[0];
        ciphertext[j + 1] = lr[1];
      }
    }

    const hashBytes = new Uint8Array(24);
    for (let i = 0; i < 6; i++) {
      hashBytes[i * 4] = (ciphertext[i] >> 24) & 0xff;
      hashBytes[i * 4 + 1] = (ciphertext[i] >> 16) & 0xff;
      hashBytes[i * 4 + 2] = (ciphertext[i] >> 8) & 0xff;
      hashBytes[i * 4 + 3] = ciphertext[i] & 0xff;
    }

    const computedHash = `$${prefix}$${costStr}$${saltEncoded}${this.encodeBase64(hashBytes, 23)}`;

    if (computedHash.length !== hashedPassword.length) return false;
    return crypto.timingSafeEqual(Buffer.from(computedHash), Buffer.from(hashedPassword));
  }
}

export default BcryptEngine;
